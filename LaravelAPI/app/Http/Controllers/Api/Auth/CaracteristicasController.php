    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function search($search, Request $request)
    {
        try {
            $searchQuery = trim($search);
            $requestData = ['id','nombre','tipo_id','estado'];
            $caracteristicas = Caracteristicas::with(['tipos_caracteristicas'])->where(function ($q) use ($requestData, $searchQuery) {
                foreach ($requestData as $field)
                    $q->orWhere($field, 'like', "%{$searchQuery}%");
            })->paginate($request->paginator, ['*'], 'page', $request->page);
            if ($caracteristicas) {
                return response([
                    'status' => 'success',
                    'code' => 1,
                    'data' => $caracteristicas
                ], 200);
            } else {
                return response([
                    'status' => 'error',
                    'code' => 0,
                    'data' => "No record found"
                ], 404);
            }
        } catch (\Exception $exception) {
            return response([
                'status' => 'error',
                'code' => 0,
                'message' => "Failed to get caracteristicas, please try again. {$exception->getMessage()}"
            ], 500);
        }
    }
